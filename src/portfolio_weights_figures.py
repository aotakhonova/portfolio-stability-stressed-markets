def target_return(mu_hat):
    return TARGET_MULTIPLIER * mu_hat.mean()


def solve_mean_variance(mu_hat, cov_hat, target):
    """
    Constrained mean-variance optimization.

    Objective:
        minimize w' Sigma w

    Constraints:
        sum(w) = 1
        w' mu >= target
        0 <= w_i <= 0.10
    """
    n = len(mu_hat)

    x0 = np.repeat(1 / n, n)

    bounds = [(LOWER_BOUND, UPPER_BOUND) for _ in range(n)]

    constraints = [
        {"type": "eq", "fun": lambda w: np.sum(w) - 1},
        {"type": "ineq", "fun": lambda w: np.dot(w, mu_hat) - target},
    ]

    cov_stable = cov_hat + 1e-8 * np.eye(n)

    result = minimize(
        fun=lambda w: np.dot(w, cov_stable @ w),
        x0=x0,
        method="SLSQP",
        bounds=bounds,
        constraints=constraints,
        options={
            "ftol": 1e-9,
            "maxiter": 500,
            "disp": False
        }
    )

    if not result.success:
        raise RuntimeError(result.message)

    return result.x


def solve_robust_mean_variance(mu_hat, cov_hat, hist_returns, target):
    """
    Robust mean-variance optimization.

    Robust expected return:
        mu_robust = mu_hat - lambda * SE(mu_hat)
    """
    se_mu = hist_returns.std(ddof=1).values / np.sqrt(len(hist_returns))
    mu_robust = mu_hat - ROBUST_LAMBDA * se_mu

    return solve_mean_variance(mu_robust, cov_hat, target)


def solve_cvar(hist_returns, mu_hat, target):
    """
    CVaR optimization solved by linear programming.

    Variables:
        w_1, ..., w_n, zeta, u_1, ..., u_T

    Objective:
        minimize zeta + 1 / ((1-alpha)T) * sum(u_t)

    Loss definition:
        L_t(w) = -r_t'w
    """
    R = hist_returns.values
    T, n = R.shape

    c = np.zeros(n + 1 + T)

    # zeta coefficient
    c[n] = 1.0

    # auxiliary variables coefficient
    c[n + 1:] = 1.0 / ((1.0 - CVAR_ALPHA) * T)

    # Equality constraint: sum weights = 1
    A_eq = np.zeros((1, n + 1 + T))
    A_eq[0, :n] = 1.0
    b_eq = np.array([1.0])

    A_ub = []
    b_ub = []

    # Target return:
    # w' mu >= target  ->  -w' mu <= -target
    row = np.zeros(n + 1 + T)
    row[:n] = -mu_hat
    A_ub.append(row)
    b_ub.append(-target)

    # CVaR auxiliary constraints:
    # u_t >= loss_t - zeta
    # loss_t = -R_t w
    # -R_t w - zeta - u_t <= 0
    for t in range(T):
        row = np.zeros(n + 1 + T)
        row[:n] = -R[t, :]
        row[n] = -1.0
        row[n + 1 + t] = -1.0
        A_ub.append(row)
        b_ub.append(0.0)

    A_ub = np.array(A_ub)
    b_ub = np.array(b_ub)

    bounds = []

    # Portfolio weight bounds
    for _ in range(n):
        bounds.append((LOWER_BOUND, UPPER_BOUND))

    # zeta is unrestricted
    bounds.append((None, None))

    # u_t auxiliary variables are non-negative
    for _ in range(T):
        bounds.append((0, None))

    result = linprog(
        c=c,
        A_ub=A_ub,
        b_ub=b_ub,
        A_eq=A_eq,
        b_eq=b_eq,
        bounds=bounds,
        method="highs"
    )

    if not result.success:
        raise RuntimeError(result.message)

    return result.x[:n]


# =====================================================
# RUN ROLLING BACKTEST TO GENERATE WEIGHTS
# =====================================================

dates = returns.loc[START_OOS:END_OOS].index

weights_mv = []
weights_cvar = []
weights_robust = []
used_dates = []
failed_dates = []

for date in dates:
    date_position = returns.index.get_loc(date)

    if date_position < ROLLING_WINDOW:
        continue

    hist = returns.iloc[date_position - ROLLING_WINDOW:date_position]

    # Skip only if the current 60-month window has missing values.
    # Do not delete whole industry columns from the full dataset.
    if hist.isna().any().any():
        failed_dates.append((date, "Missing values in 60-month estimation window"))
        continue

    mu_hat = hist.mean().values
    cov_hat = hist.cov().values
    target = target_return(mu_hat)

    try:
        w_mv = solve_mean_variance(mu_hat, cov_hat, target)
        w_cvar = solve_cvar(hist, mu_hat, target)
        w_robust = solve_robust_mean_variance(mu_hat, cov_hat, hist, target)

        weights_mv.append(w_mv)
        weights_cvar.append(w_cvar)
        weights_robust.append(w_robust)
        used_dates.append(date)

    except Exception as e:
        failed_dates.append((date, str(e)))


weights_mv = pd.DataFrame(weights_mv, index=used_dates, columns=returns.columns)
weights_cvar = pd.DataFrame(weights_cvar, index=used_dates, columns=returns.columns)
weights_robust = pd.DataFrame(weights_robust, index=used_dates, columns=returns.columns)

print("Successful months:", len(used_dates))
print("Failed months:", len(failed_dates))

if failed_dates:
    print("First few failed dates:")
    print(failed_dates[:10])


