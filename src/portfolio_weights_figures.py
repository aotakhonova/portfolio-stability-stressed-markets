def load_kf_49_monthly(zip_path):
    """
    Loads monthly value-weighted returns from the Kenneth French
    49 Industry Portfolios zip file.

    Important:
    This version keeps all 49 industry columns.
    It does not drop columns with missing values.
    """
    with zipfile.ZipFile(zip_path, "r") as z:
        csv_name = z.namelist()[0]
        raw = z.read(csv_name).decode("latin1")

    lines = raw.splitlines()

    start = None
    for i, line in enumerate(lines):
        if "Average Value Weighted Returns -- Monthly" in line:
            start = i
            break

    if start is None:
        raise ValueError("Could not find monthly value-weighted returns section.")

    header = lines[start + 1].split(",")
    rows = []

    for line in lines[start + 2:]:
        if not line.strip():
            break

        parts = line.split(",")

        if len(parts) < 2:
            break

        date_value = parts[0].strip()

        if re.fullmatch(r"\d{6}", date_value):
            rows.append(parts)
        else:
            break

    df = pd.DataFrame(rows, columns=header)
    df.rename(columns={df.columns[0]: "Date"}, inplace=True)

    df["Date"] = pd.to_datetime(df["Date"], format="%Y%m")

    for col in df.columns[1:]:
        df[col] = pd.to_numeric(df[col], errors="coerce")

    df = df.set_index("Date")

    # Replace Kenneth French missing-value codes with NaN
    df = df.replace([-99.99, -999, -999.0], np.nan)

    # Do NOT drop columns.
    # The thesis uses the 49 Industry Portfolios, so all columns should remain.
    df = df / 100.0

    return df


